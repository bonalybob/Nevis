import helperFunctions from '@/scripts/si/helperFunctions'

test('Short Byte', () => {
  expect(helperFunctions.shortByte(0X02)).toBe('02')
  expect(helperFunctions.shortByte(0XFF)).toBe('ff')
  expect(helperFunctions.shortByte(0X00)).toBe('00')
  expect(helperFunctions.shortByte(0XB4)).toBe('b4')
  expect(helperFunctions.shortByte(0X36)).toBe('36')
})

test('Combine Bytes', () => {
  expect(helperFunctions.combineBytes([0x00, 0x00, 0x00])).toBe(0)
  expect(helperFunctions.combineBytes([0x00, 0x00, 0x01])).toBe(1)
  expect(helperFunctions.combineBytes([0xFF, 0xFF, 0xFF])).toBe(16777215)
  expect(helperFunctions.combineBytes([0x00])).toBe(0)
  expect(helperFunctions.combineBytes([0xFF])).toBe(255)
  expect(helperFunctions.combineBytes([0x36])).toBe(54)
  expect(helperFunctions.combineBytes([0xC3])).toBe(195)
  expect(helperFunctions.combineBytes([0x12, 0x54])).toBe(4692)
  expect(helperFunctions.combineBytes([0xF3, 0xC2])).toBe(62402)
  expect(helperFunctions.combineBytes([136, 193])).toBe(35009)
})

test('Check Undefined', () => {
  expect(helperFunctions.checkUndefined([0x12, 0x54])).toBeFalsy()
  expect(helperFunctions.checkUndefined([0xF3, 0xC2])).toBeFalsy()
  expect(helperFunctions.checkUndefined([0xFF, 0xFF, 0xFF])).toBeFalsy()
  expect(helperFunctions.checkUndefined([0x00])).toBeFalsy()
  expect(helperFunctions.checkUndefined([0xFF])).toBeFalsy()
  expect(helperFunctions.checkUndefined([0x36])).toBeFalsy()
  expect(helperFunctions.checkUndefined([0xC3])).toBeFalsy()
  expect(helperFunctions.checkUndefined([0xEE])).toBeFalsy()
  expect(helperFunctions.checkUndefined([0xEE, 0xEE])).toBeTruthy()
})

test('Validity Check', () => {
  expect(helperFunctions.validityCheck([2, 229, 6, 0, 10, 0, 4, 171, 95, 44, 177, 3])).toBeTruthy()
  expect(helperFunctions.validityCheck([2, 231, 6, 0, 10, 0, 4, 171, 95, 12, 189, 3])).toBeTruthy()
  expect(helperFunctions.validityCheck([2, 229, 6, 0, 10, 0, 4, 88, 222, 133, 184, 3])).toBeTruthy()
  expect(helperFunctions.validityCheck([2, 231, 6, 0, 10, 0, 4, 88, 222, 165, 180, 3])).toBeTruthy()
  expect(helperFunctions.validityCheck([2, 231, 6, 0, 10, 0, 4, 88, 221, 165, 190, 3])).toBeTruthy()
  expect(helperFunctions.validityCheck([2, 230, 6, 0, 10, 0, 4, 171, 95, 44, 177, 3])).toBeFalsy()
  expect(helperFunctions.validityCheck([2, 231, 6, 1, 10, 0, 4, 171, 95, 12, 189, 3])).toBeFalsy()
  expect(helperFunctions.validityCheck([1, 2, 3, 4, 5, 6, 7, 8])).toBeFalsy()
  expect(helperFunctions.validityCheck([2, 231, 6, 0, 1, 165, 180, 3])).toBeFalsy()
  expect(helperFunctions.validityCheck([2, 229, 6, 51, 110, 0, 4, 825, 2214, 133, 178, 3])).toBeFalsy()
  expect(helperFunctions.validityCheck([2, 231, 6, 0, 10, 0])).toBeFalsy()
})

